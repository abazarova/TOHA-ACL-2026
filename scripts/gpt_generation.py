async def main(generations_path: Path, dataset: str):
    df = pd.read_csv(generations_path / "generations.csv")
    annotation_prompt = prompt[dataset]
    results = []
    async for idx, row in tqdm_asyncio(df.iterrows()):
        if dataset == "CoQA":
            query_str = f"#Context#: {row['context']} \n #Question#: {row['question']} \n #Answer#: {row['generated_answer']} \n #Halllucination#:"
        elif dataset == "SQuAD":
            query_str = f"Context: {row['context']} \n Question: {row['question']} \n Correct answer: {row['answer']} \n LLM-generated answer: {row['generated_answer']} \n Annotation:"
        elif dataset == "XSum":
            query_str = f"Reference text: {row['document']}\n Generated summary: {row['generated_summary']}\n Does this summary contain hallucinations?"
        query_text = annotation_prompt + query_str
        label = await answer(query_text, system_prompt="")
        results.append((idx, label))

    # Updating DataFrame with the results
    for idx, label in results:
        df.at[idx, "hallucination"] = label

    df.to_csv(generations_path / "annotation.csv")


