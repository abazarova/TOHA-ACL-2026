def answers_to_labels(answer: str) -> int:
    if re.search("No", answer, re.IGNORECASE):
        return 0
    elif re.search("Yes", answer, re.IGNORECASE):
        return 1
    return np.nan


def eval_predictions(predictions_df: pd.DataFrame) -> dict:
    df = predictions_df.copy()
    df["gpt_hallucination"] = df["gpt_hallucination"].apply(answers_to_labels)
    df = df[~(df["gpt_hallucination"].isnull())]

    y_true = df["hallucination"].tolist()
    y_pred = df["gpt_hallucination"].tolist()

    return {
        "accuracy": accuracy_score(y_true, y_pred),
        "precision": precision_score(y_true, y_pred),
        "recall": recall_score(y_true, y_pred),
    }


async def compose_messages(
    query_text: str,
    system_prompt: str = "",
) -> list[dict]:
    if system_prompt != "":
        messages = [
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": query_text},
        ]
    else:
        messages = [{"role": "user", "content": query_text}]

    return messages


async def answer(
    query_text: str,
    model: str = "gpt-4o",
    system_prompt: str = "Вы деловой чат-бот помощник",
) -> str:
    messages = await compose_messages(query_text, system_prompt)

    response = await client.chat.completions.create(model=model, messages=messages)

    # return 'Placeholder ChatGPT Answer'
    return response.choices[0].message.content


